    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        
        try {
            Connect a = new Connect();
            Connection conn = a.getConnection();
            PreparedStatement ps = conn.prepareStatement("select * from TaiKhoan where TenDangNhap=? and MatKhau=?");
            ps.setString(1,txtTaiKhoan.getText());
            ps.setString(2,txtMatKhau.getText());
            st=ps.executeQuery();
            
            if(txtTaiKhoan.getText().equals("")||txtMatKhau.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Chưa nhập tài khoản hoặc mật khẩu");
            }
            else if(st.next()){
                FrmTrangChu tc = new FrmTrangChu(st.getInt("MaQuyen"));
                tc.setVisible(true);
                this.dispose(); 
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
            }
            else{
                JOptionPane.showMessageDialog(this, "Đăng nhập thất bại");
            }
            
        } catch (Exception ex){ 
            System.err.println("Lỗi rồi");
        }
          
    }//GEN-LAST:event_btnDangNhapActionPerformed

    
